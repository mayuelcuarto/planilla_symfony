<?php

namespace PlanillaBundle\Entity;

/**
 * MaestroDetalle
 */
class MaestroDetalle
{
    /**
     * @var string
     */
    private $codMaestro;

    /**
     * @var string
     */
    private $codDetalle;

    /**
     * @var string
     */
    private $nombre;

    /**
     * @var string
     */
    private $estado;

    /**
     * @var string
     */
    private $descripcio;


    /**
     * Set codMaestro
     *
     * @param string $codMaestro
     *
     * @return MaestroDetalle
     */
    public function setCodMaestro($codMaestro)
    {
        $this->codMaestro = $codMaestro;

        return $this;
    }

    /**
     * Get codMaestro
     *
     * @return string
     */
    public function getCodMaestro()
    {
        return $this->codMaestro;
    }

    /**
     * Set codDetalle
     *
     * @param string $codDetalle
     *
     * @return MaestroDetalle
     */
    public function setCodDetalle($codDetalle)
    {
        $this->codDetalle = $codDetalle;

        return $this;
    }

    /**
     * Get codDetalle
     *
     * @return string
     */
    public function getCodDetalle()
    {
        return $this->codDetalle;
    }

    /**
     * Set nombre
     *
     * @param string $nombre
     *
     * @return MaestroDetalle
     */
    public function setNombre($nombre)
    {
        $this->nombre = $nombre;

        return $this;
    }

    /**
     * Get nombre
     *
     * @return string
     */
    public function getNombre()
    {
        return $this->nombre;
    }

    /**
     * Set estado
     *
     * @param string $estado
     *
     * @return MaestroDetalle
     */
    public function setEstado($estado)
    {
        $this->estado = $estado;

        return $this;
    }

    /**
     * Get estado
     *
     * @return string
     */
    public function getEstado()
    {
        return $this->estado;
    }

    /**
     * Set descripcio
     *
     * @param string $descripcio
     *
     * @return MaestroDetalle
     */
    public function setDescripcio($descripcio)
    {
        $this->descripcio = $descripcio;

        return $this;
    }

    /**
     * Get descripcio
     *
     * @return string
     */
    public function getDescripcio()
    {
        return $this->descripcio;
    }
}

