<?php

namespace PlanillaBundle\Entity;

/**
 * Grpf
 */
class Grpf
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var integer
     */
    private $anoEje;

    /**
     * @var string
     */
    private $grpf;

    /**
     * @var string
     */
    private $nombre;

    /**
     * @var boolean
     */
    private $estado;

    /**
     * @var boolean
     */
    private $esPresupu;

    /**
     * @var string
     */
    private $cadena;
    
    /**
     * Get cadena
     *
     * @return string
     */
    public function getCadena()
    {
        return $this->getAnoEje() ." - ".$this->nombre;
    } 

    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set anoEje
     *
     * @param integer $anoEje
     *
     * @return Grpf
     */
    public function setAnoEje($anoEje)
    {
        $this->anoEje = $anoEje;

        return $this;
    }

    /**
     * Get anoEje
     *
     * @return integer
     */
    public function getAnoEje()
    {
        return $this->anoEje;
    }

    /**
     * Set grpf
     *
     * @param string $grpf
     *
     * @return Grpf
     */
    public function setGrpf($grpf)
    {
        $this->grpf = $grpf;

        return $this;
    }

    /**
     * Get grpf
     *
     * @return string
     */
    public function getGrpf()
    {
        return $this->grpf;
    }

    /**
     * Set nombre
     *
     * @param string $nombre
     *
     * @return Grpf
     */
    public function setNombre($nombre)
    {
        $this->nombre = $nombre;

        return $this;
    }

    /**
     * Get nombre
     *
     * @return string
     */
    public function getNombre()
    {
        return $this->nombre;
    }

    /**
     * Set estado
     *
     * @param boolean $estado
     *
     * @return Grpf
     */
    public function setEstado($estado)
    {
        $this->estado = $estado;

        return $this;
    }

    /**
     * Get estado
     *
     * @return boolean
     */
    public function getEstado()
    {
        return $this->estado;
    }

    /**
     * Set esPresupu
     *
     * @param boolean $esPresupu
     *
     * @return Grpf
     */
    public function setEsPresupu($esPresupu)
    {
        $this->esPresupu = $esPresupu;

        return $this;
    }

    /**
     * Get esPresupu
     *
     * @return boolean
     */
    public function getEsPresupu()
    {
        return $this->esPresupu;
    }
}

